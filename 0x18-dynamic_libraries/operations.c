#include "operations.h"
#include <stdio.h>

/**
* add - adds two integers
* @a: first integer
* @b: second integer
* Return: addition
*/
int add(int a, int b)
{
return (a + b);
}

/**
* sub - function to find differnece of two integers
* @a: first integer
* @b: second integer
* Return: subtraction
*/

int sub(int a, int b)
{
return (a - b);
}

/**
* mul- multiplies two integers
* @a: first integer
* @b: second integer
* Return: multiplication
*/

int mul(int a, int b)
{
return (a * b);
}

/**
* div - function that divides two integers
* @a: first integer
* @b: second integer
* Return: division
*/

int div(int a, int b)
{
return (a / b);
}

/**
* mod - modulus of two integers
* @a: first integer
* @b: second integer
* Return: modulus
*/

int mod(int a, int b)
{
return (a % b);
}
